import turtle

wn = turtle.Screen()
wn.title("PT projects")
wn.bgcolor("yellow")


t = turtle.Turtle()
t.shape("circle")

# dish for cake
t.penup()
t.setpos(-300, -50)

t.speed(1)
t.begin_fill()
t.fillcolor("red")
t.forward(600)
t.left(90)
t.forward(10)
